
namespace Codewars.Solutions._8_Kyu
{
    ///Very simple, given a number, find its opposite.
    ///
    ///Examples:
    ///
    ///1: -1
    ///14: -14
    ///-34: 34
    
    class OppositeNumber
    {
        public static int Solution(int number)
        {
            return -number;
        }
    }
}
